end;

# ╔═╡ 023ae564-36b3-4b2d-9ede-c2feca60c672
function recall(ŷ::Vector{<:Number}, y::Vector{<:Number})
	TP = true_positive(ŷ, y)
	FN = false_negative(ŷ, y)
	return (TP + eps(typeof(TP))) / (TP + FN + eps(typeof(TP)))
end;

# ╔═╡ d05be475-5bf4-4604-8746-f44bd80f861e
function precision(ŷ::Vector{<:Number}, y::Vector{<:Number})
	TP = true_positive(ŷ, y)
	FP = false_positive(ŷ, y)
	return (TP + eps(typeof(TP))) / (TP + FP + eps(typeof(TP)))
end;

# ╔═╡ be6b5cfc-4eb5-43f7-8b32-4af2ab41c526
function IoU(ŷ::Vector{<:Number}, y::Vector{<:Number})
	TP = true_positive(ŷ, y)
	FP = false_positive(ŷ, y)
	FN = false_negative(ŷ, y)
	ϵ = eps(typeof(TP))
	return (TP + ϵ) / (TP + FP + FN + ϵ)
end;

# ╔═╡ 07151940-e03a-493d-965d-40a6da92dd22
begin
	struct SegmentationMetric
		metric::Function
		weights::Vector{Float32}
	end

	function (m::SegmentationMetric)(ŷ::Array{<:Number,4}, y::Array{<:Number,4})
		@assert size(ŷ) == size(y)
		@assert length(m.weights) == size(ŷ)[3]
		rows, cols, nclasses, obs = size(ŷ)
		ŷ = hcat([ŷ[r,c,:,o] for r in 1:rows for c in 1:cols for o in 1:obs]...)
		y = hcat([y[r,c,:,o] for r in 1:rows for c in 1:cols for o in 1:obs]...)
		metrics = [m.metric(ŷ[class,:], y[class,:]) for class in 1:nclasses]
		return sum(metrics .* m.weights)
	end

end;

