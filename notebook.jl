# ╔═╡ 509dbb7a-2cde-4810-9541-df0cfa37ff77
begin 
	struct FourPatch <: AbstractPatch
		coords::Vector{Tuple{Int64, Int64}}
	end

	FourPatch() = FourPatch([(row, col) for col in 1:512:513 for row in 1:512:513])

	function (p::FourPatch)(img::Array{Float32, 3}, patch::Int)
		@assert 1 <= patch <= 4
		row, col = p.coords[patch]
		return img[row:row+511,col:col+511,:]
	end
end;

# ╔═╡ dcec97b7-3933-422d-81c9-39074fd1184f
begin 
	struct NinePatch <: AbstractPatch 
		coords::Vector{Tuple{Int64, Int64}}
	end

	NinePatch() = NinePatch([(row, col) for col in 1:256:513 for row in 1:256:513])

	function (p::NinePatch)(img::Array{Float32, 3}, patch::Int)
		@assert 1 <= patch <= 9
		row, col = p.coords[patch]
		return img[row:row+511,col:col+511,:]
	end
end;


